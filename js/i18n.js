function applyLanguage(lang) {
  const dict = translations[lang];

  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  document.querySelectorAll('[data-i18n-attr-content]').forEach((el) => {
    const key = el.getAttribute('data-i18n-attr-content');
    if (dict[key] !== undefined) el.setAttribute('content', dict[key]);
  });

  document.querySelectorAll('[data-i18n-attr-alt]').forEach((el) => {
    const key = el.getAttribute('data-i18n-attr-alt');
    if (dict[key] !== undefined) el.setAttribute('alt', dict[key]);
  });

  document.querySelectorAll('[data-i18n-attr-aria-label]').forEach((el) => {
    const key = el.getAttribute('data-i18n-attr-aria-label');
    if (dict[key] !== undefined) el.setAttribute('aria-label', dict[key]);
  });

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  localStorage.setItem('sigf-lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('sigf-lang');
  const initial = saved === 'en' || saved === 'no' ? saved : 'no';
  applyLanguage(initial);

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => applyLanguage(btn.getAttribute('data-lang')));
  });
});
